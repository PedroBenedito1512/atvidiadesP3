package Uber;
public class Uber {
	private String nome;
	private double saldo;
	private double nota;
	private boolean aceitar;
	
	public Uber(String nome, double saldo, double nota, boolean aceitar) {
		super();
		this.nome = nome;
		this.saldo = saldo;
		this.nota = nota;
		this.aceitar = aceitar;
	}
	
	

}
